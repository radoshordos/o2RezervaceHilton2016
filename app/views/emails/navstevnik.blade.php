<h1>Potvrzení registrace na SPOLEČENSKÝ VEČER</h1>

<p>Vážená kolegyně, vážený kolego,<br/>děkujeme vám za registraci jedné či dvou vstupenek na SPOLEČENSKÝ VEČER, který se bude konat v sobotu 19. března 2016 v pražském hotelu Hilton.</p>
<p>Zarezervované vstupenky můžete nakupovat <strong>od pondělí 29. 2. 2016</strong>, 8:00 u paní <strong>Ilony Zárubové</strong> (budova Olšanská 6, Praha 3, č. dv. A 439, tel. linka 65907) - s výjimkou úterý 1. 3. 2016.</p>
<p><strong>V úterý 1. 3. 2016</strong> od 10 do 15 hodin bude možné objednané vstupenky vyzvednout v pražské budově Gamma (9. podlaží, č. dv. B901).</p>
<p>Cena jedné vstupenky je <strong>300 Kč</strong>.</p>
<p>Místa u konkrétních stolů zaručit nelze. Vstupenky je však možné zakoupit hromadně pro více kolegů a kolegyň, se kterými byste chtěli sedět pohromadě. Při prodeji vstupenek se vám budeme snažit vyjít maximálně vstříc.</p>
<p>Pro dokreslení průběhu večera doplňujeme informaci o tom, ve kterých sálech budou jednotliví interpreti vystupovat:</p>

<ul>
    <li>Grand Ballroom (tzv. horní sál): Marie Doležalová a Marek Zelinka, Olga Lounová, Slza, DJ Martin Hrdinka</li>
    <li>Kongresový sál (tzv. dolní sál): Moondance Orchestra Martina Kumžáka, Wanastowi Vjecy, DJ Martin Hrdinka</li>
</ul>

<p>Návštěvníci mohou v průběhu večera navštěvovat oba sály nezávisle na fixním místě k sezení v jednom z nich.</p>

<h2>Vstup na akci</h2>
<p>Dovolujeme si připomenout, že při vstupu na tuto akci je nezbytné, aby alespoň jeden z páru (či jednotlivě přicházející) prokázal svou příslušnost ke společnosti <strong>předložením ID zaměstnanecké karty</strong>. Bez tohoto doložení nebude vstup na společenský večer umožněn a za vstupenky
    nebude poskytnuta náhrada. U vstupu budou instalovány čtečky karet a vstup bude umožněn pouze zaregistrovaným zaměstnancům. Zneužití služební ID karty je považováno za hrubé porušení pracovní kázně a tento průkaz bude neoprávněné osobě na místě odebrán. Prosíme o porozumění pro toto opatření,
    večer je významně dotován z prostředků sociálního fondu společnosti a má charakter uzavřené akce pouze pro zaměstnance a jejich doprovod.</p>
<p>Pokud se akce nemůžete zúčastnit, prosím, odhlaste svou registraci v aplikaci nebo u paní Ilony Zárubové, abyste uvolnili kapacitu dalším kolegyním a kolegům. V případě, že si své vstupenky nezakoupíte do úterý 8. března 2016, 12:00, bude vaše rezervace zrušena a vstupenky budou nabídnuty
    zaregistrovaným náhradníkům.</p>
